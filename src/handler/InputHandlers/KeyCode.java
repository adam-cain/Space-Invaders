package handler.InputHandlers;

/**
 * Enum representing the key codes used for input handling in the game.
 * This set of key codes includes those commonly used for actions like shooting and moving.
 */
public enum KeyCode {
    /**
     * Represents the space key, used for shooting.
     */
    SPACE,

    /**
     * Represents the 'A' key, used for moving left.
     */
    A,

    /**
     * Represents the 'D' key, used for moving right.
     */
    D,

    /**
     * Represents the left arrow key, used for moving left.
     */
    ARROW_LEFT,

    /**
     * Represents the right arrow key, used for moving right.
     */
    ARROW_RIGHT,
}
